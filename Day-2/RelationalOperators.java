import java.util.Scanner;
class RelationalOperators{
 public static void main(String[] args){
  System.out.println('a'==97);//Relational operator it checks expression true or false
  System.out.println('a'>'A');//greater than operator
  System.out.println((10%2)>=5);
  System.out.println(5<=5);//less than or equal to operator
  System.out.println((5+3)<2);//less than operator
  System.out.println(-7!=7);//not equal operator
  }
 }