class EscapeSequences{
    public static void main(String[] args) {
        System.out.println("hello\nworld"); // \n is used for new line
        System.out.println("welcome to java \f world");// \f is used for form feed
        System.out.println("welco\bme");// \b is used for back space
        System.out.println("wel\rcome");// \r is used for carriage return
        System.out.println("hello\tworld");// \t is used for tab space
    }
}